package com.example.model;

public class OperationModel {

   
    private int a;
    private int b;
  


    public OperationModel() {
    }

   
   
    public OperationModel(int a, int b) {
        this.a = a;
        this.b = b;
    }


    public int getA() {
        return a;
    }

    public void setA(int a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }

    public void setB(int b) {
        this.b = b;
    }

    

    

   
}
